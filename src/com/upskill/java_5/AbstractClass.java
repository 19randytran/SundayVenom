package com.upskill.java_5;

public abstract class AbstractClass {

	public void color(){
		String carColor = "Red";
	}
	public void door(){
		int carDoor = 4;
	}
	
	public void wheel(){
		int carWheel = 4;
	}
	
	public void engine(){
		String carEngine = "8 hp";
	}
	
	public abstract void iColor();
	
	public abstract void iDoor();
	
	public abstract void iWheel();
	
	public abstract void iEngine();
}
